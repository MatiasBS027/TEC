    private void llenarTabla() {
        try {
            InputStream is = getClass().getClassLoader().getResourceAsStream("data/mecanicos.xml");
            if (is == null) {
                throw new FileNotFoundException("No se encontr√≥ data/mecanicos.xml en el classpath");
            }
            @SuppressWarnings("unchecked")
            ArrayList<Mecanico> mecanicos = (ArrayList<Mecanico>) util.cargadorXML.Cargar(is, "mecanico");
            Vector<String> columnas = new Vector<>();
            columnas.addElement("ID");
            columnas.addElement("Nombre");
            columnas.addElement("Puesto");

            Vector<Vector<String>> filas = new Vector<>();
            for (Mecanico m : mecanicos) {
                Vector<String> fila = new Vector<>();
                fila.addElement(m.getId());
                fila.addElement(m.getNombre());
                fila.addElement(m.getPuesto());
                filas.addElement(fila);
            }
            DefaultTableModel datos = new DefaultTableModel(filas, columnas);
            this.jTable1.setModel(datos);

        } catch (FileNotFoundException e) {
            e.printStackTrace();
        }
    }


