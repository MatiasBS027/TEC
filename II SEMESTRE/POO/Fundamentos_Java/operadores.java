public class Main {
    public static void main(String[] args) {
        System.out.println("1 + 1: " + (1 + 1)); // Suma
        System.out.println("2 - 1: " + (2 - 1)); // Resta
        System.out.println("2 * 3: " + (2 * 3)); // Multiplicación
        System.out.println("6 / 2: " + (6 / 2)); // División
        System.out.println("5 % 2: " + (5 % 2)); // Módulo (resto de la división)
        System.out.println("2 + 3 * 4: " + (2 + 3 * 4)); // Prioridad de operaciones
        System.out.println("(2 + 3) * 4: " + ((2 + 3) * 4)); // Uso de paréntesis para cambiar la prioridad
        System.out.println("5 > 3: " + (5 > 3)); // Mayor que
        System.out.println("5 < 3: " + (5 < 3)); // Menor que
        System.out.println("5 >= 5: " + (5 >= 5)); // Mayor o igual que   
        System.out.println("5 <= 5: " + (5 <= 5)); // Menor o igual que
        System.out.println("5 == 5: " + (5 == 5)); // Igual a
        System.out.println("5 != 3: " + (5 != 3)); // Diferente de
        System.out.println(true && false); // AND lógico
        System.out.println(true || false); // OR lógico
        System.out.println(!true); // NOT lógico
        System.out.println("Hola, " + "mundo!"); // Concatenación de cadenas
        System.out.println("El resultado de 2 + 3 es: " + (2 + 3)); // Concatenación con expresión
    } 
}